package exos.instructions;

/**
 * Exercice S78 : De 1 à 10
 * Écrire un programme qui affiche les nombres de 1 à 10 à l'aide d'une boucle
 * Afficher la valeur à chaque itération
 */
public class ExoS78 {
    public static void main(String[] args) {
        for (int i = 1 ; i <= 10 ; i++) {
            System.out.println(i);
        }
    }
}
